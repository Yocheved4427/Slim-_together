// Returns today's task index based on a fixed epoch
function getDailyTaskIndex() {
  const epoch = new Date(2026, 0, 1).getTime(); // Jan 1 2026
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const dayNum = Math.floor((today.getTime() - epoch) / 86400000);
  return ((dayNum % TASKS.length) + TASKS.length) % TASKS.length;
}

function getDailyTask() {
  return TASKS[getDailyTaskIndex()];
}

function getRandomQuote() {
  const d = new Date();
  return QUOTES[(d.getDate() + d.getMonth()) % QUOTES.length];
}

